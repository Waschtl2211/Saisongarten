/**
 * Pflanze anhand des Namens aus der Datenbank finden (fuzzy, case-insensitive).
 */
export function findPflanze(name) {
  const lc = name.toLowerCase().trim();
  return (
    pflanzDatenbank.find(p => p.name.toLowerCase() === lc) ||
    pflanzDatenbank.find(p => p.aliases.some(a => lc === a || lc.includes(a) || a.includes(lc))) ||
    null
  );
}

/**
 * Erntbare Monate aus Pflanzliste + Pflanzdatum berechnen.
 */
export function computeErntbarIm(gepflanztStr, pflanzenListe) {
  const monate = new Set();
  for (const pflanzName of pflanzenListe) {
    const info = findPflanze(pflanzName);
    if (!info) continue;
    const gepflanzt = parseISO(gepflanztStr);
    for (let i = 0; i < info.erntedauer; i++) {
      monate.add(format(addMonths(gepflanzt, info.ernteanfangOffset + i), 'yyyy-MM'));
    }
  }
  return Array.from(monate).sort();
}

/**
 * Gibt die Nachbarschaftsrelation zwischen einer Pflanze und allen anderen im Beet zurück.
 * Rückgabe: { relation: 'gut'|'schlecht'|'neutral', mitPflanzen: string[], gegenPflanzen: string[] }
 */
export function getNeighborRelation(pflanzName, allePflanzenImBeet) {
  const info = findPflanze(pflanzName);
  if (!info) return { relation: 'neutral', gutMit: [], schlechtMit: [] };

  const andere = allePflanzenImBeet.filter(p => p !== pflanzName);

  // Bidirektional prüfen: A→B oder B→A
  const gutMit = andere.filter(p => {
    const infoP = findPflanze(p);
    return info.gut.some(g => g.toLowerCase() === p.toLowerCase())
        || (infoP?.gut || []).some(g => g.toLowerCase() === pflanzName.toLowerCase());
  });
  const schlechtMit = andere.filter(p => {
    const infoP = findPflanze(p);
    return info.schlecht.some(s => s.toLowerCase() === p.toLowerCase())
        || (infoP?.schlecht || []).some(s => s.toLowerCase() === pflanzName.toLowerCase());
  });

  let relation = 'neutral';
  if (schlechtMit.length > 0) relation = 'schlecht';
  else if (gutMit.length > 0) relation = 'gut';

  return { relation, gutMit, schlechtMit };
}
